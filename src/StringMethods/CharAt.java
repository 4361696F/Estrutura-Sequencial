package StringMethods;

public class CharAt {
    public static void main(String[] args) {
        String frase = "Hello World";
        char primeiraLetraDaPrimeiraPalavra = frase.charAt(0);//
        System.out.println(primeiraLetraDaPrimeiraPalavra);
        char primeiraLetraDaSegundaPalavra = frase.charAt(6);
        System.out.println(primeiraLetraDaSegundaPalavra);
    }
}
